    for (CsVehicle data : list) {
      map = new HashMap<String, Object>();
      map.put("value", data.getCsvId());
      map.put("text", data.getCsvVin() + "(" + data.getCsvCarNo() + ")");
      mapList.add(map);
    }
    return VoResult.success().setValue(mapList);
